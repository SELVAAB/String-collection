package com.string;

public class Reverse {

	public static void main(String[] args) {
		
		
		
		
		
		String input="Geeks for geeks 123 brazil cameroon columbia argentina uruguavy combodia france german italy portugal netherland spain belgium whales england  ";
		
		
		
		
		StringBuilder input1=new StringBuilder();
		
		
		input1.append(input);
		
		
		input1.reverse();
		
		
		
		
		
	
		
		System.out.println(input1);
	}

}
