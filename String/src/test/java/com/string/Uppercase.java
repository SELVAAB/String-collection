package com.string;

public class Uppercase {

	public static void main(String[] args) {
		String team="groupa,groupb,groupc";
		
		String mmm=team.toUpperCase();
		System.out.println(mmm);
		
		String match="GROUPA,GROUPB,GROUPC";
		
		String fixture=match.toLowerCase();
		
		System.out.println(fixture);

	}

}
